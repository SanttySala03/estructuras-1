    private void ingresar() {
        String nombre = cjNombre.getText();
        String categoria = cjIdentificacion.getText();
        int ano = Integer.parseInt(cjFechaNacimiento.getText());

        Videojuegos nuevoVideojuego = new Videojuegos(nombre, categoria, ano);
        videojuegosList.add(nuevoVideojuego);

        cjNombre.setText("");
        cjIdentificacion.setText("");
        cjFechaNacimiento.setText("");

        JOptionPane.showMessageDialog(this, "Videojuego ingresado", "ATENCIÃ“N", JOptionPane.INFORMATION_MESSAGE);
        actualizaListado();
    }


    private void atender() {
        JOptionPane.showMessageDialog(this, "Atender", "ATENCION", JOptionPane.INFORMATION_MESSAGE);
        this.actualizaListado();
    }
    private void actualizaListado() {
        StringBuilder listado = new StringBuilder();
        for (Videojuegos videojuego : videojuegosList) {
            listado.append(videojuego.toString()).append("\n");
        }
        cjListado.setText(listado.toString());
    }

